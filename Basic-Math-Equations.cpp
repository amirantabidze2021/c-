#include <iostream>
using namespace std;
int main() {
	int i1, i2;
	cout<<"Enter a number: ";
	cin>>i1;
	cout<<"Enter a second number: ";
	cin>>i2;
	cout<<i1<<"+"<<i2<<"=";
	cout<<i1+i2<<"\n";
	cout<<i1<<"-"<<i2<<"=";
	cout<<i1-i2<<"\n";
	cout<<i1<<"*"<<i2<<"=";
	cout<<i1*i2<<"\n";
	cout<<i1<<"/"<<i2<<"=";
	cout<<i1/i2<<"\n";
	return 0;
}
